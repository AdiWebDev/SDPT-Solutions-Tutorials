// Increment - Adds 1 to a variable. 
// Decreemnt - Subtracts 1 to a variable. 

// Increment Operator 
/* 
 Prefix 
 ++variable - so nagdadagdag muna siya ng 1 
 
 Postfix
 variable++  - after ng isang condition saka siya nadadagdag
   
*/  

// Decrement Operator 
/* 
 Prefix 
 --variable - so nagbabawas muna siya ng 1 
 
 Postfix  
 variable-- - after ng isang condition saka siya magbabawas
   
*/