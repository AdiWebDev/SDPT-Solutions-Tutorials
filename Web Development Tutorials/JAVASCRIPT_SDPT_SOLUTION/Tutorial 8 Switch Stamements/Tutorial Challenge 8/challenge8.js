/* Creaate a program that outputs the equivalent day of a number variable 

1 - Monday
2 - Tuesday 
3 - Wednesday
4 - Thursday 
5 - Friday 
6 - Saturday 
7 - Sunday */ 

let day = 8; 

switch(day){ 
    case 1: 
        alert("Monday"); 
        break;
    case 2: 
        alert("Tueday"); 
        break;
    case 3: 
        alert("Wednesday"); 
        break;
    case 4: 
        alert("Thursday"); 
        break;
    case 5: 
        alert("Friday"); 
        break;
    case 6: 
        alert("Saturday"); 
        break;
    case 7: 
        alert("Sunday"); 
        break;
    default: 
        alert("Invalid Number"); 
        break;
    }