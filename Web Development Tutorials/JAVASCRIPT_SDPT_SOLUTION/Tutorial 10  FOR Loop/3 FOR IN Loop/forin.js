/* FOR/IN Loop 
- Are used for iterating over JSONs or Arrays. 
- It returns the key or index of each item. 
- Puro index ang lalabas dito*/ 

let people = ["David", "Alenere", "Gilbert", "James"]
for(let i in people){  
    // if index gusto idisplay.
    console.log(i); 
    // if value gusto idisplay. 
    console.log(people[i]);
}